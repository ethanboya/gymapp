// Regenerates src/data/exerciseCatalogExtra.js from the free-exercise-db
// public domain dataset (https://github.com/yuhonas/free-exercise-db).
// Run with: node scripts/generate-exercise-catalog.mjs

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const SOURCE_URL = 'https://raw.githubusercontent.com/yuhonas/free-exercise-db/main/dist/exercises.json'
const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'data', 'exerciseCatalogExtra.js')

// imageIds already used by the hand-curated exercises in src/data/models.js - skip these to avoid duplicates
const existingImageIds = new Set([
  'Barbell_Bench_Press_-_Medium_Grip',
  'Incline_Dumbbell_Press',
  'Dumbbell_Shoulder_Press',
  'Butterfly',
  'Triceps_Pushdown',
  'Side_Lateral_Raise',
  'Pullups',
  'Bent_Over_Barbell_Row',
  'Barbell_Deadlift',
  'Barbell_Squat',
  'Leg_Press',
  'Lying_Leg_Curls',
  'Dumbbell_Lunges',
  'Dumbbell_Bicep_Curl',
  'Standing_Dumbbell_Triceps_Extension',
  'Dumbbell_Flyes',
  'Wide-Grip_Lat_Pulldown',
  'Face_Pull'
])

const MUSCLE_GROUP_MAP = {
  chest: 'Chest',
  shoulders: 'Shoulders',
  triceps: 'Arms',
  biceps: 'Arms',
  forearms: 'Arms',
  lats: 'Back',
  'middle back': 'Back',
  'lower back': 'Posterior Chain',
  traps: 'Upper Back',
  quadriceps: 'Legs',
  hamstrings: 'Legs',
  glutes: 'Legs',
  calves: 'Legs',
  adductors: 'Legs',
  abductors: 'Legs',
  abdominals: 'Core',
  neck: 'Shoulders'
}

const EQUIPMENT_MAP = {
  'body only': 'Bodyweight',
  barbell: 'Barbell',
  dumbbell: 'Dumbbell',
  machine: 'Machine',
  cable: 'Cable',
  bands: 'Bands',
  kettlebells: 'Kettlebell',
  'exercise ball': 'Exercise Ball',
  'e-z curl bar': 'EZ Curl Bar',
  'foam roll': 'Foam Roller',
  'medicine ball': 'Medicine Ball',
  other: 'Other'
}

function toEquipment(value) {
  if (!value) return 'Other'
  return EQUIPMENT_MAP[value] || 'Other'
}

function toMuscleGroup(primaryMuscles) {
  const first = primaryMuscles?.[0]
  return MUSCLE_GROUP_MAP[first] || 'Other'
}

function toDescription(instructions) {
  if (!Array.isArray(instructions) || !instructions.length) return ''
  return instructions.join(' ')
}

async function main() {
  const response = await fetch(SOURCE_URL)
  if (!response.ok) throw new Error(`Failed to fetch dataset: ${response.status}`)
  const raw = await response.json()

  const filtered = raw.filter((e) => Array.isArray(e.images) && e.images.length > 0 && !existingImageIds.has(e.id))

  // NOTE: plain object literals on purpose (not createExercise(...)) - models.js
  // imports this file, so importing createExercise back from models.js here
  // would create a circular import that breaks at module-evaluation time.
  let out = `// AUTO-GENERATED by scripts/generate-exercise-catalog.mjs\n`
  out += `// Source: https://github.com/yuhonas/free-exercise-db (public domain). Do not hand-edit.\n\n`
  out += `export const exerciseCatalogExtra = [\n`

  for (const e of filtered) {
    out += `  {\n`
    out += `    id: ${JSON.stringify(e.id)},\n`
    out += `    name: ${JSON.stringify(e.name)},\n`
    out += `    muscleGroup: ${JSON.stringify(toMuscleGroup(e.primaryMuscles))},\n`
    out += `    equipment: ${JSON.stringify(toEquipment(e.equipment))},\n`
    out += `    imageId: ${JSON.stringify(e.id)},\n`
    out += `    description: ${JSON.stringify(toDescription(e.instructions))}\n`
    out += `  },\n`
  }

  out += `]\n`

  fs.writeFileSync(OUTPUT_PATH, out)
  console.log(`Wrote ${filtered.length} exercises to ${path.relative(process.cwd(), OUTPUT_PATH)}`)
  console.log(`Skipped ${raw.length - filtered.length} already covered by the hand-curated catalog`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
